#!/bin/bash
# 真实数据报告生成系统
# 版本: 1.0
# 日期: 2026-04-03

set -e

# 配置
REPORT_DIR="/root/.openclaw/workspace/memory/reports/LNG/daily_estimates"
DATA_DIR="/root/.openclaw/workspace/memory/reports/LNG/raw_data"
REPORT_DATE=$(date +%Y-%m-%d)
TIMESTAMP=$(date +%Y%m%d_%H%M%S)

# 创建目录
mkdir -p "$REPORT_DIR/$REPORT_DATE"

echo "=== 真实数据报告生成系统 ==="
echo "生成时间: $(date '+%Y-%m-%d %H:%M:%S')"
echo "报告日期: $REPORT_DATE"
echo "数据标准: 真实数据优先，禁止估算"
echo "================================="

# 报告文件
MARKDOWN_REPORT="$REPORT_DIR/$REPORT_DATE/LNG市场真实报告_$REPORT_DATE.md"
HTML_REPORT="$REPORT_DIR/$REPORT_DATE/LNG市场真实报告_网页版_$REPORT_DATE.html"
JSON_DATA="$REPORT_DIR/$REPORT_DATE/real_market_data_$REPORT_DATE.json"

# 读取最新审核报告
latest_audit=$(ls -t /root/.openclaw/workspace/memory/reports/LNG/audit_logs/audit_enhanced_*.md 2>/dev/null | head -1)
if [ -n "$latest_audit" ]; then
    audit_score=$(grep "总体得分" "$latest_audit" | grep -o "[0-9]\+/100" | cut -d'/' -f1)
    echo "基于审核报告: $(basename $latest_audit)"
    echo "审核得分: ${audit_score:-N/A}/100"
else
    echo "⚠️ 警告: 未找到审核报告，数据质量无法保证"
    audit_score=0
fi

# 数据质量警告
if [ "${audit_score:-0}" -lt 70 ]; then
    echo "⚠️ ⚠️ ⚠️ 重要警告: 数据质量不足 (得分: ${audit_score}/100)"
    echo "当前报告可能包含需要更新的数据"
    echo "建议配置实时数据源后再生成正式报告"
    echo ""
    DATA_QUALITY_WARNING="⚠️ **数据质量警告**: 当前报告基于部分需要更新的数据生成，建议配置实时数据源API以获得更准确的信息。"
else
    DATA_QUALITY_WARNING="✅ **数据质量**: 报告基于审核通过的数据生成。"
fi

# 生成Markdown报告
echo "生成Markdown报告: $MARKDOWN_REPORT"

cat > "$MARKDOWN_REPORT" << EOF
# LNG市场真实数据报告
## 报告日期: $REPORT_DATE
## 生成时间: $(date '+%Y-%m-%d %H:%M:%S')
## 数据标准: 真实数据优先，禁止估算

$DATA_QUALITY_WARNING

**重要提示**: 本报告尽可能使用真实数据，但部分数据源需要API配置才能获取实时信息。

